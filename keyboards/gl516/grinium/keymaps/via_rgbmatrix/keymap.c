bool encoder_update_user(uint8_t index, bool clockwise) {
  keypos_t key;

  dprintf("index=%u, clockwise=%d\n", index, clockwise);

  key.row = 4;

  if (index == 0) {
    if (clockwise) {
      key.col = 1;
    } else {
      key.col = 0;
    }
    tap_code16(keymap_key_to_keycode(layer_switch_get_layer(key), key));
  }

  if (index == 1) {
    if (clockwise) {
      key.col = 3;
    } else {
      key.col = 2;
    }
    tap_code16(keymap_key_to_keycode(layer_switch_get_layer(key), key));
  }

  if (index == 2) {
    if (clockwise) {
      key.col = 5;
    } else {
      key.col = 4;
    }
    tap_code16(keymap_key_to_keycode(layer_switch_get_layer(key), key));
  }

  return false;
}

